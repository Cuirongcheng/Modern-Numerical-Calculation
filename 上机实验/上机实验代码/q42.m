A=[4 -1 0 -1 0 0;-1 4 -1 0 -1 0;0 -1 4 -1 0 -1;-1 0 -1 4 -1 0;0 -1 0 -1 4 -1;0 0 -1 0 -1 4];
b=[0 5 -2 5 -2 6];
x0=[0 0 0 0 0 0];
max1=100;%迭代次数
err=10^(-4);%精度
m=GSDD(A,b,max1,x0,err);
for j=1:length(m)
fprintf('n=%d\t x=[%f %f %f %f %f %f]\n',j,m(j,1),m(j,2),m(j,3),m(j,4),m(j,5),m(j,6));
end